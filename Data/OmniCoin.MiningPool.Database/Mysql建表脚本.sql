CREATE TABLE IF NOT EXISTS `Miners`(
	`Id` BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT 'Id',
	`Address` VARCHAR(64) NOT NULL COMMENT '钱包地址',
	`Account` VARCHAR(64) NOT NULL COMMENT '姓名',
	`Type` INT NOT NULL DEFAULT 0 COMMENT '类型 0：POS，1：手机',
	`SN` VARCHAR(64) NOT NULL COMMENT '设备序列号',
	`Status` INT NOT NULL DEFAULT 0 COMMENT '状态 0：enable，1：disable',
	`Timstamp` BIGINT NOT NULL DEFAULT 0 COMMENT '写入时间戳',
	`LastLoginTime` BIGINT NOT NULL DEFAULT 0 COMMENT '最后登陆时间时间戳'
)ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT='矿工表';

CREATE TABLE IF NOT EXISTS `Blocks`(
	`Id` BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT 'Id',
	`Hash` VARCHAR(64) NOT NULL COMMENT '区块Hash',
	`Height` BIGINT NOT NULL DEFAULT 0 COMMENT '区块高度',
	`Timstamp` BIGINT NOT NULL DEFAULT 0 COMMENT '区块生成时间',
	`Generator` VARCHAR(64) NOT NULL COMMENT '生成区块矿工钱包地址',
	`Nonce` BIGINT NOT NULL DEFAULT 0 COMMENT '生成区块随机数',
	`TotalReward` BIGINT NOT NULL DEFAULT 0 COMMENT '总的奖励',
	`TotalHashes` BIGINT NOT NULL DEFAULT 0 COMMENT '总的Hash数，总的工作量',
	`Confirmed` INT NOT NULL DEFAULT 0 COMMENT '是否确认，检查当前区块是否有效',
	`IsDiscarded` INT NOT NULL DEFAULT 0 COMMENT '区块是否作废 0：正常， 1：已作废',
  `IsRewardSend` INT NOT NULL DEFAULT 0 COMMENT '区块奖励是否发放 0：未发放，1：已发放'
)ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT='区块表';

CREATE TABLE IF NOT EXISTS `RewardList`
(
	`Id` BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT 'Id',
	`BlockHash` VARCHAR(64) NOT NULL COMMENT '区块Hash',
	`MinerAddress` VARCHAR(64) NOT NULL COMMENT '钱包地址',
	`Hashes` BIGINT NOT NULL DEFAULT 0 COMMENT 'Hash个数',
	`OriginalReward` BIGINT NOT NULL DEFAULT 0 COMMENT '原始奖励',
	`ActualReward` BIGINT NOT NULL DEFAULT 0 COMMENT '实际奖励',
	`Paid` INT NOT NULL DEFAULT 0 COMMENT '是否支付 0：未支付，1已支付',
	`GenerateTime` BIGINT NOT NULL DEFAULT 0 COMMENT '生成时间时间戳',
	`PaidTime` BIGINT NOT NULL DEFAULT 0 COMMENT '支付时间时间戳',
	`TransactionHash` VARCHAR(64) NOT NULL COMMENT '交易Hash',
	`Commission` BIGINT NOT NULL DEFAULT 0 COMMENT '提成奖励',
	`IsCommissionProcessed` INT NOT NULL DEFAULT '0' COMMENT '提成是否发放',
	`CommissionProcessedTime` BIGINT COMMENT '提成发放时间'
)ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT='奖励信息表';